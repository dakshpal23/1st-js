// Basic if-else--
// And and Or
 
const month = 23;
switch (month) {
    case 1:
        console.log('january');
        break;
    case 2:
        console.log('february');
        break;
    case 3:
        console.log('march');
        break;

    default:
        console.log('default Case');
        
        break;
}